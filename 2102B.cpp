void solve()
{
    ll n;
    cin >> n;
    vi arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    for (int i = 0; i < n; i++)
    {
        arr[i] = abs(arr[i]);
    }
    ll cnt = 0;
    for (int i = 0; i < n; i++)
    {
        if (arr[i] > arr[0])
        {
            cnt++;
        }
    }
    if (cnt >= ((n + 1) / 2) - 1)
    {
        yes;
    }
    else
    {
        no;
    }
}

